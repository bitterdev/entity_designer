namespace Bitter\EntityDesigner;

use Concrete\Core\Routing\RouteListInterface;
use Concrete\Core\Routing\Router;

class RouteList implements RouteListInterface
{
    public function loadRoutes(Router $router)
    {
        $router
            ->buildGroup()
            ->setNamespace('Concrete\Package\EntityDesigner\Controller\Dialog\Support')
            ->setPrefix('/ccm/system/dialogs/entity_designer')
            ->routes('dialogs/support.php', 'entity_designer');
    }
}